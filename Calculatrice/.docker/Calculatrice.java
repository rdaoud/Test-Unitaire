public class Calculatrice{


    public int add(int a, int b){
        int resultat;
        resultat = a + b;
        return resultat;
    }

    public int sub(int a, int b){
        int resultat;
        resultat = a - b;
        return resultat;
    }

    public int mul(int a, int b){
        int resultat;
        resultat = a * b;
        return resultat;
    }

    public int div(int a, int b){
        int resultat;
        resultat = a / b;
        return resultat;
    }
    

}